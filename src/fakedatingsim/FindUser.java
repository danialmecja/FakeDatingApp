    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        boolean numeric = true;
        try {
            Double num = Double.parseDouble(rangeText.getText());
        } catch (NumberFormatException e) {
            numeric = false;
        }
        if(numeric){
            errorLabel.setVisible(false);
            listModel.clear();
            listModel.removeAllElements();
            String sentence;
            FriendZoneUser temp;
            getList();
            sortList(user.getInterest(),otherInt);
            ListRepresentation whichRange = new ListRepresentation(opSentence,null);
            listModel.addElement(whichRange);
            for(int i = 0; i < otherInt.size(); i++){
                temp = list.get(otherInt.get(i));
                sentence = temp.getFirstName() + " " + temp.getLastName() + ", " + temp.getAge() + " " + temp.getGender() + ", lives in " + temp.getCity() + ", compatability is " + otherInt.get(i).get(5);
                listModel.addElement(new ListRepresentation(sentence, temp));
            } 
        }
        else{
            errorLabel.setVisible(true);
        }
    }//GEN-LAST:event_searchButtonActionPerformed


    public void getList(){
        otherInt.clear();
        initialList.clear();    
        list.clear();
        Search imp = new Search(database, user, Double.parseDouble((String)rangeText.getText()),male,female);
        initialList = imp.increaseRad();
        opSentence = imp.getOpSent();
        for(int i = 0; i < initialList.size(); i++)
            otherInt.add(initialList.get(i).getInterest()); 
    }
    
     public Double radEq(Double x, Double h, Double y, Double k){

        Double rad;

        rad = sqrt((pow(x-h, 2) + pow(y-k, 2)));

        return rad;
    }
    
     public ArrayList<ArrayList<String>> sortList(ArrayList<String> realUser, ArrayList<ArrayList<String>> dummyUser){
         for(int i = 0; i < dummyUser.size(); i ++)
        {
            int counter = 0;
            for(int j = 0; j < dummyUser.get(i).size(); j ++)
            {
                for(int k = 0; k < realUser.size(); k++)
                {
                    if(dummyUser.get(i).get(j).equals(realUser.get(k)))
                    {
                        counter++;
                    }
                }
                
            }
            dummyUser.get(i).add(Integer.toString(counter));
            list.put(dummyUser.get(i), initialList.get(i));
        }
        SortInterest sort = new SortInterest(2, dummyUser);//Create new SortInterest class
        dummyUser = sort.selectionSort();//Sort the ArrayList and is returned
        return dummyUser; 
     }
     
    public void setHash(){
        String line;
        try{
            BufferedReader br = new BufferedReader(new FileReader("locations.txt"));
            while((line = br.readLine()) != null){
                String[] a = line.split(",");
                String[] b = a[1].split("!");
                double x = Double.parseDouble(b[0]), y = Double.parseDouble(b[1]);
                double[] loc = {x,y};
                coord.put(a[0], loc);
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(FindUser.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FindUser.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    
